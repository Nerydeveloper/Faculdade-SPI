#Uma variável é um nome que se refere a um local na memória onde você pode armazenar 
#e recuperar dados. Uma variável é usada para representar valores, como números, texto,
# objetos e muito mais, em um programa. Quando você cria uma variável, você está, essencialmente, 
#reservando espaço na memória para armazenar um valor.

#EXEMPLOS:

#Representa números inteiros. Exemplos:
numeroInteiro = int

#Representa números decimais. Exemplos:
numeroReal = float

#Representa sequências de caracteres. Pode ser definida com aspas simples ou duplas. Exemplos:
palavra = str

#boolean Representa valores lógicos, True (verdadeiro) ou False (falso). Exemplos:
verdadeiro = True
#boolean Representa valores lógicos, True (verdadeiro) ou False (falso). Exemplos:
falso = False

#LISTA - Representa uma coleção ordenada de valores que podem ser de diferentes tipos. Exemplos:
lista = list

#TUPLAS - Similar a uma lista, mas é imutável, ou seja, seus elementos não podem ser alterados após a criação. Exemplos:
tuplas = tuple

#dicionario - Representa uma coleção de pares chave-valor. Cada chave é única e associada a um valor. Exemplos:
dicionario = dict









#MOSTRANDO NO TERMINAL OS TIPOS DAS VARIAVEIS
print(type(numeroInteiro))
print(type(numeroReal))
print(type(palavra))
print(type(verdadeiro))
print(type(falso))
print(type(lista))

print(type(dicionario))
