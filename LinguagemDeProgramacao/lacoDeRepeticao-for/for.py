#EXEMPLO 02
# O segundo loop "for" itera sobre uma lista de frutas chamada lista. 
# Para cada fruta na lista, ele imprime "Fruta:" seguido pelo nome da fruta.
lista = ["Banana","Uva","Macaxeira","Melancia","Melão","Maçã","Pera","Morango"]
for i in lista:
    print("Fruta: ",i)
    #Isso imprimirá o nome de cada fruta na lista.