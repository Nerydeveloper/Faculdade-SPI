#EXEMPLO 01

# A função range(10) gera uma sequência de números de 0 a 9. 
#Em cada iteração, ele imprime "Número:" seguido pelo valor de i.
for i in range(10):
    print("Número: ", i)
    #Isso imprimirá os números de 0 a 9.

